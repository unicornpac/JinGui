def seed():
    db = SessionLocal()
    try:
        existing_count = db.query(MedicalCase).count()
        print(f"当前病案数: {existing_count}")

        created = 0
        skipped = 0
        for c in CASES:
            exists = db.query(MedicalCase).filter(MedicalCase.title == c["title"]).first()
            if exists:
                skipped += 1
                continue
            case = MedicalCase(**c)
            db.add(case)
            created += 1
        
        db.commit()
        print(f"新增病案: {created}, 跳过(已存在): {skipped}")
        print(f"当前病案总数: {db.query(MedicalCase).count()}")
        
        # 显示各难度分布
        for level in ["初级", "中级", "高级"]:
            count = db.query(MedicalCase).filter(MedicalCase.difficulty_level == level).count()
            print(f"  {level}: {count} 例")
            
    finally:
        db.close()

if __name__ == "__main__":
    seed()
